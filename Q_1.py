# Program to add two numbers.

'''program with pre-defined variables'''
num1 = 22;
num2 = 55;

sum = num1 + num2;

print("The sum of two numbers is : ",sum);

'''program with user-defined'''
num1 = float(input("Enter the first number : "));
num2 = float(input("Enter the second number : "));

sum = num1 + num2;

print("The sum of two numbers is : ",sum);